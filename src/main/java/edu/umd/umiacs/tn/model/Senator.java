package edu.umd.umiacs.tn.model;

public class Senator {
	public int id;
	public String name;

	// getter and setter methods

	public Senator(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
