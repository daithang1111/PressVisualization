package edu.umd.umiacs.tn.model;

public class Senator {
	public String id;
	public String name;

	// getter and setter methods

	public Senator(String id, String name) {
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
